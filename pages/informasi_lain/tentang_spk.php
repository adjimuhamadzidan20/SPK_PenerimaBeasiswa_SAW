    	<h5 class="mb-3">Tentang Sistem Pendukung Keputusan (SPK)</h5>
        <div class="text-justify font-weight-light"> 
          <p>
          	<b>Sistem Penunjang Keputusan (SPK)</b> merupakan sebuah sistem yang dimana untuk membuat sebuah keputusan dalam menentukan penerimaan beasiswa tahfidz di sekolah, terutama di SD Negeri 1 Birayang berdasarkan kriteria-kriteria yang telah ditentukan. Sistem Penunjang Keputusan ini menggunakan metode / algoritma <b>Simple Additive Weighting (SAW)</b>. metode SAW ini berfungsi untuk penyelesaian dalam menentukan siswa yang terpilih serta mendapatkan beasiswa tahfidz. 
          </p>
          <p>
          	Pengertian Metode SAW (Simple Additive Weighting) merupakan salah satu metode dalam pengambilan keputusan multi kriteria yang sederhana dan klasik. Metode ini termasuk dalam metode pembobotan atau dikenal sebagai metode penjumlahan terbobot. Konsep dasar metode SAW adalah mencari penjumlahan terbobot dari rating kinerja pada setiap alternatif pada semua atribut.
          </p>
          <p>
          	Metode SAW membutuhkan proses normalisasi matriks keputusan (X) ke suatu skala yang dapat diperbandingkan dengan semua rating alternatif yang ada. Metode SAW mengenal adanya 2 (dua) atribut yaitu kriteria keuntungan <b>(Benefit)</b> dan kriteria biaya <b>(Cost)</b>. Perbedaan dari kedua kriteria ini adalah dalam pemilihan kriteria ketika mengambil keputusan. jika menggunakan atribut keuntungan (Benefit) maka akan mencari nilai tertinggi (MAX), sedangkan atribut biaya (Cost) maka akan mencari nilai terendah (MIN).
          </p>		
